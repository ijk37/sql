CREATE TABLE Artist (
  ArtistID varchar(10) NOT NULL,
  Name varchar(25) DEFAULT NULL,
  PRIMARY KEY (ArtistID)
);

CREATE TABLE Music (
  ArtistID varchar(10) NOT NULL,
  SongID varchar(10) NOT NULL,
  PRIMARY KEY (ArtistID,SongID)
);

CREATE TABLE Song (
  SongID varchar(10) NOT NULL,
  Title varchar(50) DEFAULT NULL,
  PRIMARY KEY (SongID)
);


